
package Entidad;


public class ClaseConArray {
    
    private String[] arrayPalabras;

    public ClaseConArray() {
        
    }

    public ClaseConArray(String[] arrayPalabras) {
        this.arrayPalabras = arrayPalabras;
    }

    public String[] getArrayPalabras() {
        return arrayPalabras;
    }

    public void setArrayPalabras(String[] arrayPalabras) {
        this.arrayPalabras = arrayPalabras;
    }

    @Override
    public String toString() {
        return "ClaseConArray{" + "arrayPalabras=" + arrayPalabras + '}';
    }
    
    
    
    
    
    
    
    
}
